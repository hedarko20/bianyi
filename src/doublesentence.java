
public class doublesentence {
String code;
String property;
public doublesentence()
{}
public doublesentence(String code,String property)
{
this.code=code;
this.property=property;
}
public doublesentence(String code)
{
this.code=code;
this.property=null;
}
public String getCode()
{
	return this.code;
	}

public String getProperty()
{
return this.property;	
}

}
